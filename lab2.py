n = int(input("Введите номер места в плацкартном вагоне: "))
if n<54:
    if n % 2 == 0:
        if n<= 36:
            print("Место", n, "- верхнее в купе")
        else:
            print("Место", n, "- верхнее боковое")
    else:
        if n <= 36:
            print("Место", n, "- нижнее в купе")
        else:
            print("Место", n, "- нижнее боковое")
else:
    print("Введён некорректный номер")